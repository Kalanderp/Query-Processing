import matplotlib.pyplot as plt
import numpy as np
np.random.seed(10)
weight1 = np.random.uniform(55.5, 62.5, 12)
height1 = np.random.uniform(95, 200, 12)
weight2 = np.random.uniform(60, 68.5, 12)
height2 = np.random.uniform(100, 195, 12)
weight3 = np.random.uniform(67, 72, 8)
height3 = np.random.uniform(130, 240, 8)
plt.figure(figsize=(6, 5))
plt.scatter(weight1, height1, marker='*', color='blue')
plt.scatter(weight2, height2, marker='*', color='blue')
plt.scatter(weight3, height3, marker='*', color='blue')
plt.title("Group wise Weight vs Height scatter plot", fontsize=18)
plt.xlabel("weight", fontsize=16)
plt.ylabel("height", fontsize=16)
plt.xlim(55, 72.5)
plt.ylim(90, 240)
plt.gca().set_facecolor("#E8E8E8")
plt.show()
